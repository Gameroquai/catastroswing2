/* Creo tabla RESERVAS */
DROP TABLE RESERVAS CASCADE CONSTRAINTS;
CREATE TABLE RESERVAS(
  DIA NUMBER(2),
  MES NUMBER(2),
  HORA NUMBER(1),
  CODAULA NUMBER(4),
  CODGRUPO VARCHAR2(15),
  DNIPROF  VARCHAR2(10),
  CONSTRAINT PK_RES PRIMARY KEY(DIA,MES,HORA,CODAULA),
  CONSTRAINT FK_RES1 FOREIGN KEY(CODAULA) REFERENCES AULAS,
  CONSTRAINT FK_GRUPO FOREIGN KEY(CODGRUPO) REFERENCES GRUPOS,
  CONSTRAINT FK_RES2 FOREIGN KEY (DNIPROF) REFERENCES PROFES);

